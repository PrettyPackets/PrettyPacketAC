function PrettyConsoleLog(type, message)
    if type == "info" then
        colorCode = "^5" 
    elseif type == "warn" then
        colorCode = "^3" 
    elseif type == "error" then
        colorCode = "^8" 
    elseif type == "debug" then
        colorCode = "^9" 
    elseif type == "success" then
        colorCode = "^2" 
    end
    print("^6[PrettyPacketAC^6]" .. colorCode .. " - " .. message .. "^7")
end


